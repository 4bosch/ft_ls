#include "print.h"

void	print_files(t_list **tab, char c)
{
	int i;

	if (c)
	{
		i = -1;
		while (tab[++i] != NULL)
		{
			ft_printf("%s\n", ((t_file*)tab[i]->content)->name);
		}
	}
	else
	{
		return ;
	}
}
